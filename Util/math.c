float sinus(int16_t x)
{
    while(x < 0) {
        x += 360;
    }
    while(x > 360) {
        x -= 360;
    }

    if(x < 90) {
        return Sinus[x];
    } else if(x < 180) {
        return Sinus[180 - x];
    } else if(x < 270) {
        return -Sinus[x - 180];
    } else {
        return -Sinus[360 - x];
    }
}

float cosinus(int16_t x)
{
    return sinus(x + 90);
}
